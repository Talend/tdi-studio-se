package org.talend.mscrm;

public enum ConditionOperator {

    /**
     * The "eq" operator
     */
    Equal,
    /**
     * The "ne" operator
     */
    NotEqual,
    /**
     * The "gt" operator
     */
    GreaterThan,
    /**
     * The "ge" operator
     */
    GreaterEqual,
    /**
     * The "lt" operator
     */
    LessThan,
    /**
     * The "le" operator
     */
    LessEqual

}
